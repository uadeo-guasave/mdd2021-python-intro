# formato snake_case
nombre = "Bidkar"
apellidos = "Aragón Cárdenas"

# Concatenación
nombre_completo = nombre + " " + apellidos
print("Nombre completo:", nombre, apellidos)
print(type(nombre))

# puedes multiplicar cadenas
nombre_x4 = nombre * 4
print("nombre_x4:", nombre_x4)


# Numericos
entero = 100
decimal = 34.98
print(type(entero))
print(type(decimal))

# Booleans
debe_materias = True
print(type(debe_materias))
if debe_materias:
    print("El alumno debe materias")
